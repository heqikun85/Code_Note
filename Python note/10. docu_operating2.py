# 扩展访问模式

f = open('test.txt', 'a+')
# r+
# r+模式下可以使用write，会将原本内容覆盖，但只覆盖写入的字符数长度。
# 超过写入的字符数长度不会被覆盖。
f.write('axdsfas')
# 由于在读取write函数时已经读取了一部分文件内容，所以以下代码会在write内容之后开始读取。
print(f.read())

# w+
# write模式下不能使用read()
# w+模式不会报错，但是会把文件内容全部覆盖掉
print(f.read()) 

# a+
# a+模式下如果没有找到文件，也会新建文件
# 如果文件存在，但a模式下文件指针在结尾，所以也无法读取文件。
print(f.read())

f.close()